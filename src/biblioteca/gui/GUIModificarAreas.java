    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
      if(jList1.getSelectedIndex()!=-1){
            if(!areasSeleccionadas.contains(areasExistentes.get(jList1.getSelectedIndex()))){
                areasSeleccionadas.add(areasExistentes.get(jList1.getSelectedIndex()));
                areasAgregadas.add(areasexistentesarray.get(jList1.getSelectedIndex()));
            }
        }
        refreshAreas();
}//GEN-LAST:event_AgregarActionPerformed



    private void Agregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Agregar1ActionPerformed
       if(jList2.getSelectedIndex()!=-1){
              areasSeleccionadas.removeElementAt(jList2.getSelectedIndex());
              areasEliminadas.add(areasSeleccionadasarray.get(jList2.getSelectedIndex()));
            }

        refreshAreas();
    }//GEN-LAST:event_Agregar1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i=new ControladorArea().agregarAreasporUsuario(username, areasAgregadas);
        int j=new ControladorArea().eliminarAreasporUsuario(username, areasEliminadas);
        if(i!=-1 && j!=-1){

            JOptionPane.showMessageDialog(this, "Areas modificadas con Exito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            parent.setVisible(true);
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed


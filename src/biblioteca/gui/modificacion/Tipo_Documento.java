    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        if(checkEmptyFieldsTipo()){
            if(new ControladorTipoDocumento().agregarTipoDocumento(Tipo_Nombre.getText().toLowerCase(), Tipo_Descripcion.getText())!=-1){
                JOptionPane.showMessageDialog(this, Tipo_Nombre.getText()+" ha sido agregado", "Notificaci贸n", JOptionPane.INFORMATION_MESSAGE);
                Tipo_Nombre.setText("");
                Tipo_Descripcion.setText("");
                initComboBox();
            }
            else{
                JOptionPane.showMessageDialog(this, Tipo_Nombre.getText()+" ya existe en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_AgregarActionPerformed

    private void SiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SiguienteActionPerformed
        if(Tipos_Documento.getSelectedIndex()!=-1){
            biblioteca.gui.GUIModificacionDocumento.documento.setTipoMaterial(tiposExistentes.get(Tipos_Documento.getSelectedIndex()).getTipoDocumento());
            biblioteca.gui.GUIModificacionDocumento.Tipo_Documento_Guardado=true;
            Tipos_Documento.setEnabled(false);
            Estado.setForeground(Color.green);
            Estado.setText("[Guardado]");
            JTabbedPane parent =(JTabbedPane) this.getParent();
            parent.setSelectedIndex(5);
            Editar.setEnabled(true);
        }
        else JOptionPane.showMessageDialog(this, "Debe seleccionar un tipo de documento", "Error", JOptionPane.ERROR_MESSAGE);
        
}//GEN-LAST:event_SiguienteActionPerformed

    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        Tipos_Documento.setEnabled(true);        
        biblioteca.gui.GUIModificacionDocumento.Tipo_Documento_Guardado=false;
        Editar.setEnabled(false);
        Estado.setForeground(Color.red);
        Estado.setText("[Sin Guardar]");
    }//GEN-LAST:event_EditarActionPerformed

    private boolean checkEmptyFieldsTipo(){
        if(Tipo_Nombre.getText()==null || Tipo_Nombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "El campo Nombre no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(Tipo_Descripcion.getText()==null || Tipo_Descripcion.getText().equals("")){
            JOptionPane.showMessageDialog(this, "El campo Descripcion no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else return true;
    }
    
    
